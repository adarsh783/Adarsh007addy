class patte
{
public static void main(String[] args)
{
int i,j,k,m,n,o;
for(i=3;i>=0;i--)
{
for(j=3;j>i;j--)
{
System.out.print(" ");
}
for(k=0;k<=i;k++)
{
System.out.print("*");
}
System.out.print("\n");
}
for(m=0;m<=3;m++)
{
for(n=3;n>m;n--)
{
System.out.print(" ");
}
for(o=0;o<=m;o++)
{
System.out.print("*");
}
System.out.print("\n");
}
}
}



